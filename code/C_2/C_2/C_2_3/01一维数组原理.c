#include <stdio.h>
int main01()
{
	int as[]={10,20,30,40,50};
	/*1 数组名的真正作用：数组整个存储空间的首地址,也代表第0个元素的地址*/
	//数组名 代表 第0个成员的空间地址
	//数组名+整数 代表 数组内成员间的偏移
	printf("%d %d %d\n", as, as + 1, as + 2);
    printf("%d %d %d\n", &as[0], &as[1], &as[2]);
 	/*2 数组名+偏移量操作成员空间*/
	printf("%d %d %d", *(as), *(as + 1), *(as + 2));
 	/*3 深度理解[] 作为运算符的作用：就是指针移动*/
	printf("%d %d", as[1], 1[as]);
	printf("%d", (as + 1)[2]);
	return 0;
}