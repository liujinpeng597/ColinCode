#include <stdio.h>
int main02()
{
	int as[] = { 10,20,30,40,50 };
	int* p;
	/*1  int型指针 指向int型数组 的意义*/
	p = as;
	/*2 比较 指针+1与数组名+1的区别 */
	printf("%d %d\n", p + 1, p + 2);// 固定地址 + 偏移
	//p++;//p是指针变量，可以指向任意空间
	//as++;//数组名是个指针（地址）常量，代表找到数组空间的唯一依据
	/*3 深度理解：
	数组名+[下标] 与 *(指针+移动) 与 指针+[下标] 的应用效果*/
	for (int i = 0; i < 5; i++) {
		printf("%d %d %d %d\n", as[i], *(as + i), *(p + i), p[i]);
	}
	/*4 利用指针变量实现数组的便利操作*/
	for (int i = 0; i < 5; i++) {
        printf("%d", *p++);
	}
	

	return 0;
}